package com.october2021.Kalkulator;

public abstract class Nilai {
	protected int nilaiA;
	protected int nilaiB;

	public int getNilaiA() {
		return nilaiA;
	}
	
	public void setNilaiA(int nilaiA) {
		this.nilaiA = nilaiA;
	}
	
	public int getNilaiB() {
		return nilaiB;
	}
	
	public void setNilaiB(int nilaiB) {
		this.nilaiB = nilaiB;
	}
}
